# Entrada de dados
a = int(input("Primeiro valor: "))
b = int(input("Segundo valor: "))

# Condicional caso o primeiro número seja maior
if a > b:
    print("O primeiro valor é maior!")
# Condicinal caso o segundo número seja maior
if b > a:
    print("O segundo valor é maior!")
# Condicional caso os números sejam iguais
if a == b:
    print("Os valores são iguais")
