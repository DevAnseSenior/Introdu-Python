# Programa que faz uma soma simples entre dois valores inteiros informados pelo usuário
n1 = int(input("Informe o primeiro número: "))  #Variável do tipo inteiro
n2 = int(input("Informe o segundo número: "))

soma = n1 + n2 # Calculo da soma e armazenamento de resultado em variável

print("Soma: %d + %d = %d" % (n1, n2, soma)) # Exibição de resultado da soma 


